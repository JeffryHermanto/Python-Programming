# Numbers

# Integer
x = 1

# Float
x = 1.1

# Complex Number
x = 1 + 2j

print(10 + 3)  # Addition
print(10 - 3)  # Substraction
print(10 * 3)  # Multiplication
print(10 / 3)  # Division in float result
print(10 // 3)  # Division in integer result
print(10 % 3)  # Modulus (remainder of division)
print(10 ** 3)  # Exponent (keft to the power of right)

# Augmented Assignment Operator
x = 10
